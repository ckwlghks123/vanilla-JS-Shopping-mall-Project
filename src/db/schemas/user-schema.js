import { Schema } from "mongoose";

const UserSchema = new Schema(
  {
    email: {
      type: String,
      required: true,
    },
    fullName: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    address: {
      type: new Schema(
        {
          postalCode: String,
          address1: String,
          address2: String,
        },
        {
          _id: false,
        }
      ),
      required: true,
    },
    isAdmin: {
      type: Boolean,
      required: false,
      default: "false",
    },
  },
  {
    collection: "users",
    timestamps: true,
  }
);

export { UserSchema };
